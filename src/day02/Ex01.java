package day02;

public class Ex01 {
	public static void main(String[] args) {
		int age = 30; // int 는 정수 // = 는 대입연산자, 오른쪽의 값을 왼쪽에 대입
		double weight = 55.0 , height = 170.3; // double은 실수 // ;(세미콜론) 문장 마지막에 넣어줘야 함
		//double height = 170.3; // 콤마로 연달아 쓸 수 있다
		System.out.println("나의 나이는 " + age + "살 입니다");
		System.out.println("나의 몸무게는 " + weight + "kg 입니다");
		System.out.println("나의 키는 " + height + "cm 입니다");
		}
}
